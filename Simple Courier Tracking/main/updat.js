//update track num for desktop
function Updatedesktno()
{
var gettno=document.getElementsByName("tnodeskupdate");
var trackingnum;
for(var i=0;i<gettno.length;i++){
    trackingnum=gettno[i].value;
}
//
if(trackingnum.length==0){
    swal("PLEASE ENTER TRACKING NUMBER","","error");
} else{
    var xmlhttp = new XMLHttpRequest();
    xmlhttp.onreadystatechange = function() {
      if (this.readyState == 4 && this.status == 200) {
         if(this.responseText=='success')
         {
             swal("TRACKING NUMBER UPDATED SUCCESSFULLY","","success");
         }else if(this.responseText=='failed'){
             swal("FAILED TO UPDATE TRACKING NUMBER!! PLEASE TRY AGAIN LATER","","error");
         }else if(this.responseText=='updated'){
            swal("ALREADY UPDATED","","success");
         }else{
        swal(this.responseText,"","warning");
        }
      }
    };
    xmlhttp.open("POST", "updatetno.php?q=" + trackingnum, true);
    xmlhttp.send();
}
}

